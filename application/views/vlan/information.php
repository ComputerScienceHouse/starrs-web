		<h1>VLANs</h1>
		<hr />
		<p>A datacenter is commonly broken up into multiple logical networks across a single set of physical hardware. These logical networks are referred to as VLANs. STARRS can track VLAN information and pull data from your network devices that may be relevant.</p>
		<p>To view an existing VLAN, select it on the left. To create a new VLAN, select a Datacenter on the left then click Create VLAN.<p>
